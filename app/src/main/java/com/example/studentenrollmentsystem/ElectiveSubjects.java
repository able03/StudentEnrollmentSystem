package com.example.studentenrollmentsystem;

public class ElectiveSubjects
{

    private String elective1;
    private String elective2;

    public ElectiveSubjects(String elective1, String elective2) {
        this.elective1 = elective1;
        this.elective2 = elective2;
    }

    public String getElective1() {
        return elective1;
    }

    public String getElective2() {
        return elective2;
    }
}
